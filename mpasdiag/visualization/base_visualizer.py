    def _interpolate_to_grid(self: "MPASVisualizer",
                             lon: np.ndarray,
                             lat: np.ndarray,
                             data: np.ndarray,
                             lon_min: float,
                             lon_max: float,
                             lat_min: float,
                             lat_max: float,
                             grid_resolution: Optional[float] = None,
                             dataset: Optional[xr.Dataset] = None,
                             method: str = 'linear',
                             resolution_bounds: Optional[Tuple[float, float]] = None,) -> Tuple[np.ndarray, np.ndarray, np.ndarray]:
        """
        This method performs interpolation of scattered data points defined by longitude, latitude, and corresponding values onto a regular lat/lon grid within specified geographic bounds. It uses the remap_mpas_to_latlon_with_masking function from MPASRemapper to handle the interpolation, which is optimized for MPAS data and can apply masking to exclude invalid points. The method allows for automatic selection of grid resolution based on the geographic extent, with optional bounds to prevent excessively coarse or fine grids. The resulting interpolated data is returned as 2D arrays corresponding to the meshgrid of longitude and latitude coordinates, suitable for use in contour or pcolormesh plots on map projections. This functionality is essential for visualizing MPAS data that may be originally defined on an unstructured grid or at irregular locations, enabling consistent and accurate representation on regular lat/lon maps. 

        Parameters:
            lon (np.ndarray): 1D array of longitude coordinates for the input data points in degrees.
            lat (np.ndarray): 1D array of latitude coordinates for the input data points in degrees.
            data (np.ndarray): 1D array of data values corresponding to each (lon, lat) point, typically representing a variable of interest (e.g., temperature, wind speed).
            lon_min (float): Western longitude bound in degrees for the interpolation grid extent.
            lon_max (float): Eastern longitude bound in degrees for the interpolation grid extent.
            lat_min (float): Southern latitude bound in degrees for the interpolation grid extent.
            lat_max (float): Northern latitude bound in degrees for the interpolation grid extent.
            grid_resolution (Optional[float]): Desired grid resolution in degrees for the output lat/lon grid. If None, the method will automatically select a resolution based on the geographic extent (default: None).
            dataset (Optional[xr.Dataset]): Optional xarray Dataset containing the original MPAS grid information, which can be used by MPASRemapper for more accurate interpolation. If None, the method will attempt to create a minimal dataset from the provided lon and lat arrays (default: None).
            method (str): Interpolation method to use, options include 'nearest', 'linear', 'cubic', etc., as supported by MPASRemapper (default: 'linear').
            resolution_bounds (Optional[Tuple[float, float]]): Optional tuple specifying the minimum and maximum allowable grid resolution in degrees (default: None).

        Returns:
            Tuple[np.ndarray, np.ndarray, np.ndarray]: A tuple containing the longitude meshgrid, latitude meshgrid, and interpolated data array on the regular lat/lon grid, suitable for plotting on map projections. The longitude and latitude meshgrids correspond to the coordinates of the interpolated data points. 
        """
        if grid_resolution is not None:
            step = float(grid_resolution)
            if step <= 0:
                raise ValueError("grid_resolution must be > 0")
            resolution = step
            print(f"Using MPASRemapper with target resolution: {resolution}°")
        else:
            lon_range = lon_max - lon_min
            lat_range = lat_max - lat_min
            resolution = max(lon_range / 100, lat_range / 100)
            if resolution_bounds is not None:
                lo, hi = resolution_bounds
                resolution = max(lo, min(resolution, hi))
            print(f"Auto-selected grid resolution: {resolution:.4f}°")

        print(f"Interpolating {len(data)} points using MPASRemapper (KDTree→xESMF {method})...")

        if dataset is None:
            lon_arr = lon if isinstance(lon, np.ndarray) else lon.values
            lat_arr = lat if isinstance(lat, np.ndarray) else lat.values
            dataset = xr.Dataset({
                'lonCell': xr.DataArray(lon_arr, dims=['nCells']),
                'latCell': xr.DataArray(lat_arr, dims=['nCells'])
            })

        data_xr = xr.DataArray(data, dims=['nCells'])

        remapped_result = remap_mpas_to_latlon_with_masking(
            data=data_xr,
            dataset=dataset,
            lon_min=lon_min,
            lon_max=lon_max,
            lat_min=lat_min,
            lat_max=lat_max,
            resolution=resolution,
            method=method,
            apply_mask=True,
            lon_convention='auto'
        )

        lon_coords = remapped_result.lon.values
        lat_coords = remapped_result.lat.values
        lon_mesh, lat_mesh = np.meshgrid(lon_coords, lat_coords)
        data_interp = remapped_result.values

        print(f"MPASRemapper produced {data_interp.shape[0]}x{data_interp.shape[1]} grid")
        return lon_mesh, lat_mesh, data_interp

    def _create_scatter_plot(self: "MPASVisualizer",
                             lon: np.ndarray,
                             lat: np.ndarray,
                             data: np.ndarray,
                             cmap_obj: Union[str, mcolors.Colormap],
                             norm: Optional[mcolors.Normalize],
                             data_crs: ccrs.CRS,
                             colorbar_label: Optional[str] = None,
                             colorbar_ticks: Optional[List[float]] = None,) -> None:
        """
        This method creates a scatter plot of data points colored by their corresponding values, with an optional colorbar for reference. It calculates an adaptive marker size based on the geographic extent and density of points to ensure that the plot is informative without being overly cluttered. The method sorts the data points by their values to improve visualization of overlapping points, and applies the specified colormap and normalization for accurate color representation. It also adds a colorbar with appropriate labeling and tick formatting to provide context for interpreting the colors in terms of the underlying data values. This scatter plot is useful for visualizing spatial patterns in MPAS data, such as temperature or precipitation at specific locations, while maintaining clarity even in dense datasets. 

        Parameters:
            lon (np.ndarray): 1D array of longitude coordinates for the data points in degrees.
            lat (np.ndarray): 1D array of latitude coordinates for the data points in degrees.
            data (np.ndarray): 1D array of data values corresponding to each (lon, lat) point, typically representing a variable of interest (e.g., temperature, precipitation).
            cmap_obj (Union[str, mcolors.Colormap]): Colormap instance or name to use for coloring the data points based on their values.
            norm (Optional[mcolors.Normalize]): Optional normalization instance to control the mapping of data values to colors, allowing for custom scaling (e.g., logarithmic) if desired.
            data_crs (ccrs.CRS): Coordinate reference system for the input data points, used for accurate plotting on the map projection.
            colorbar_label (Optional[str]): Label for the colorbar to indicate the variable and units being represented by the colors (default: None, which will attempt to build a label from variable metadata if available).
            colorbar_ticks (Optional[List[float]]): Optional list of specific tick values to set on the colorbar for better readability, especially if the data range is known and a limited number of ticks is desired (default: None, which will use automatic tick placement by matplotlib).

        Returns:
            None: Modifies self.ax by adding a scatter plot of the data points colored by their values, and adds a colorbar if applicable. Raises AssertionError if self.ax or self.fig is None, indicating that the plotting area must be set up before calling this method. 
        """
        assert self.ax is not None, "Axes must be created before scatter plot"
        assert self.fig is not None, "Figure must be created before scatter plot"

        map_extent = (lon.min(), lon.max(), lat.min(), lat.max())
        fig_size = (self.figsize[0], self.figsize[1])
        marker_size = self.calculate_adaptive_marker_size(map_extent, len(data), fig_size)

        map_area = (map_extent[1] - map_extent[0]) * (map_extent[3] - map_extent[2])
        point_density = len(data) / map_area if map_area > 0 else 0
        alpha_val = 0.8 if point_density > 1000 else 0.9

        sort_indices = np.argsort(data)
        lon_sorted = lon[sort_indices]
        lat_sorted = lat[sort_indices]
        data_sorted = data[sort_indices]

        scatter = self.ax.scatter(
            lon_sorted, lat_sorted, c=data_sorted,
            cmap=cmap_obj, norm=norm, s=marker_size, alpha=alpha_val,
            transform=data_crs, edgecolors='none'
        )

        try:
            if colorbar_label is None:
                colorbar_label = MPASVisualizationStyle.build_colorbar_label(
                    getattr(self, '_current_var_metadata', None)
                )

            cbar = MPASVisualizationStyle.add_colorbar(
                self.fig, self.ax, scatter,
                label=colorbar_label,
                orientation='horizontal', extend='both',
                fraction=0.04, pad=0.06, shrink=0.8,
                fmt=None, labelpad=-60, label_pos='top', tick_labelsize=8
            )

            if cbar is not None:
                if colorbar_ticks is not None and len(colorbar_ticks) <= 15:
                    cbar.set_ticks(colorbar_ticks)
                    cbar.set_ticklabels(self._format_ticks_dynamic(colorbar_ticks))
                else:
                    ticks = cbar.get_ticks().tolist()
                    cbar.set_ticks(ticks)
                    cbar.set_ticklabels(self._format_ticks_dynamic(ticks))
                cbar.ax.tick_params(labelsize=8)
        except Exception:
            pass

