class MPASVisualizer:
    """ Base class for MPAS model output visualization with comprehensive functionality for publication-quality cartographic presentations of unstructured mesh data. """
    
    def __init__(self: "MPASVisualizer", 
                 figsize: Tuple[float, float] = (10, 14), 
                 dpi: int = 100, 
                 verbose: bool = True) -> None:
        """
        This initializer sets up the base visualizer with default figure size and resolution settings, and initializes attributes for figure and axes management. The figsize parameter allows users to specify the dimensions of the output figure in inches, with a default of (10, 14) for portrait orientation suitable for maps. The dpi parameter controls the resolution of saved images, where 100 is appropriate for screen display and 300 or higher is recommended for publication-quality output. The verbose flag enables printing of informational messages during visualization processes for debugging purposes. The method initializes self.fig and self.ax to None, which will be assigned when specific plotting methods create figures and axes. 

        Parameters:
            figsize (Tuple[float, float]): Default figure size in inches as (width, height) for all plots (default: (10, 14)).
            dpi (int): Default resolution in dots per inch for saved figures, affecting image quality and file size (default: 100).
            verbose (bool): Enable verbose output for debugging and informational messages during visualization processes (default: True). 

        Returns:
            None: Initializes instance attributes self.figsize, self.dpi, self.fig, and self.ax.
        """
        self.figsize = figsize
        self.dpi = dpi
        self.verbose: bool = verbose
        self.fig: Optional[Figure] = None
        self.ax: Optional[Union[Axes, GeoAxes]] = None
    
    def add_timestamp_and_branding(self: "MPASVisualizer") -> None:
        """
        This method adds a timestamp and MPASdiag branding annotation to the current figure for consistent identification and attribution of visualizations. It retrieves the current UTC time, formats it as a human-readable string, and uses MPASVisualizationStyle to add this timestamp along with a standardized MPASdiag logo or text annotation to the figure. This method should be called after creating a plot but before saving it to ensure that all visualizations include clear metadata about when they were generated and their association with the MPASdiag project. 

        Parameters:
            None: This method does not require any parameters and operates on the current figure (self.fig).

        Returns:
            None: Modifies self.fig by adding timestamp and branding annotations, raises AssertionError if self.fig is None. 
        """
        assert self.fig is not None, "Figure must be created before adding branding"
        MPASVisualizationStyle.add_timestamp_and_branding(self.fig)
    
    def format_latitude(self: "MPASVisualizer", 
                        value: float, _) -> str:
        """
        This method formats a numeric latitude value for axis tick labels using standardized geographic conventions with degree symbols and hemisphere indicators. It delegates to MPASVisualizationStyle to ensure consistent latitude formatting across all plot types, converting decimal degrees to readable strings with N/S suffixes (e.g., 45.0 becomes '45°N', -30.0 becomes '30°S'). The method accepts a placeholder second argument to satisfy matplotlib's FuncFormatter interface requirements, which allows it to be used directly as a formatter for axis ticks in cartopy plots. 

        Parameters:
            value (float): Latitude value in decimal degrees (-90 to 90).
            _ (Any): Placeholder parameter required by matplotlib FuncFormatter (ignored). 

        Returns:
            str: Formatted latitude string with degree symbol and hemisphere (e.g., '45°N', '30°S').
        """
        return MPASVisualizationStyle.format_latitude(value, _)

    def format_longitude(self: "MPASVisualizer", 
                         value: float, _) -> str:
        """
        This method formats a numeric longitude value for axis tick labels using standardized geographic conventions with degree symbols and hemisphere indicators. It delegates to MPASVisualizationStyle to ensure consistent longitude formatting across all plot types, converting decimal degrees to readable strings with E/W suffixes (e.g., 120.0 becomes '120°E', -75.0 becomes '75°W'). The method accepts a placeholder second argument to satisfy matplotlib's FuncFormatter interface requirements, which allows it to be used directly as a formatter for axis ticks in cartopy plots.

        Parameters:
            value (float): Longitude value in decimal degrees (-180 to 180).
            _ (Any): Placeholder parameter required by matplotlib FuncFormatter (ignored).

        Returns:
            str: Formatted longitude string with degree symbol and hemisphere (e.g., '120°E', '75°W'). 
        """
        return MPASVisualizationStyle.format_longitude(value, _)
    
    def calculate_adaptive_marker_size(self: "MPASVisualizer", 
                                       map_extent: Tuple[float, float, float, float], 
                                       num_points: int, 
                                       fig_size: Tuple[float, float] = (12, 10)) -> float:
        """
        This method calculates an adaptive marker size for scatter plot visualizations based on the geographic extent of the map, the number of data points to be plotted, and the figure size. It delegates to MPASVisualizationStyle to apply a heuristic that considers the density of points within the map area and scales the marker size accordingly to prevent overcrowding or excessive sparsity in the visualization. The method returns an appropriate marker size in matplotlib points-squared units that can be used directly in scatter plot functions to achieve a balanced visual representation of data points across different map extents and dataset sizes. 

        Parameters:
            map_extent (Tuple[float, float, float, float]): Geographic extent of the map as (lon_min, lon_max, lat_min, lat_max) in degrees.
            num_points (int): Total number of data points to be plotted within the map extent.
            fig_size (Tuple[float, float]): Size of the figure in inches as (width, height) for scaling calculations (default: (12, 10)). 

        Returns:
            float: Calculated marker size in points-squared units for use in matplotlib scatter plots, optimized for the given map extent and data point density. 
        """
        return MPASVisualizationStyle.calculate_adaptive_marker_size(map_extent, num_points, fig_size)

